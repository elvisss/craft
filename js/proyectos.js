/**
 * Filter shuffle based on the current state of filters.
 */
Demo.prototype.filter = function () {
  console.log(this.filters)
  if (this.hasActiveFilters()) {
    this.shuffle.filter(this.itemPassesFilters.bind(this));
  } else {
    this.shuffle.filter(Shuffle.ALL_ITEMS);
  }
};

/**
 * If any of the arrays in the `filters` property have a length of more than zero,
 * that means there is an active filter.
 * @return {boolean}
 */
Demo.prototype.hasActiveFilters = function () {
  return Object.keys(this.filters).some(function (key) {
    return this.filters[key].length > 0;
  }, this);
};

/**
 * Determine whether an element passes the current filters.
 * @param {Element} element Element to test.
 * @return {boolean} Whether it satisfies all current filters.
 */
Demo.prototype.itemPassesFilters = function (element) {
  var categories = this.filters.categories;
  var services = this.filters.services;
  var areas = this.filters.areas;
  var names = this.filters.names;

  var architectures = this.filters.architectures;
  var constructions = this.filters.constructions;

  var offices = this.filters.offices;
  var retails = this.filters.retails;
  var projects = this.filters.projects;
  var designs = this.filters.designs;
  var developments = this.filters.developments;
  var ejecutions = this.filters.ejecutions;

  var category = element.getAttribute('data-category');
  var service = element.getAttribute('data-service');
  var area = element.getAttribute('data-area');
  var name = element.getAttribute('data-name');

  var architecture = element.getAttribute('data-architecture');
  var construction = element.getAttribute('data-construction');

  var office = element.getAttribute('data-office');
  var retail = element.getAttribute('data-retail');
  var project = element.getAttribute('data-project');
  var design = element.getAttribute('data-design');
  var development = element.getAttribute('data-development');
  var ejecution = element.getAttribute('data-ejecution');

  // If there are active shape filters and this shape is not in that array.
  if (categories.length > 0 && !categories.includes(category)) {
    return false;
  }

  // If there are active color filters and this color is not in that array.
  if (services.length > 0 && !services.includes(service)) {
    return false;
  }

  // If there are active color filters and this color is not in that array.
  if (areas.length > 0 && !areas.includes(area)) {
    return false;
  }

  // If there are active color filters and this color is not in that array.
  if (names.length > 0 && !names.includes(name)) {
    return false;
  }

  // If there are active color filters and this color is not in that array.
  if (architectures.length > 0 && !architectures.includes(architecture)) {
    return false;
  }

  // If there are active color filters and this color is not in that array.
  if (constructions.length > 0 && !constructions.includes(construction)) {
    return false;
  }

  // If there are active color filters and this color is not in that array.
  if (offices.length > 0 && !offices.includes(office)) {
    return false;
  }

  // If there are active color filters and this color is not in that array.
  if (retails.length > 0 && !retails.includes(retail)) {
    return false;
  }

  // If there are active color filters and this color is not in that array.
  if (projects.length > 0 && !projects.includes(project)) {
    return false;
  }

  // If there are active color filters and this color is not in that array.
  if (designs.length > 0 && !designs.includes(design)) {
    return false;
  }

  // If there are active color filters and this color is not in that array.
  if (developments.length > 0 && !developments.includes(development)) {
    return false;
  }

  // If there are active color filters and this color is not in that array.
  if (ejecutions.length > 0 && !ejecutions.includes(ejecution)) {
    return false;
  }

  return true;
};

