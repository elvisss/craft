'use strict';

var Shuffle = window.Shuffle;

var Demo = function (element) {
  this.categories = Array.from(document.querySelectorAll('.js-categories button'));
  this.services = Array.from(document.querySelectorAll('.js-service li'));
  this.areas = Array.from(document.querySelectorAll('.js-areas li'));
  this.names = document.getElementById('js-names');

  this.architecture = document.getElementById('js-architecture');
  this.construction = document.getElementById('js-construction');

  this.office = document.getElementById('js-office');
  this.retail = document.getElementById('js-retail');
  this.project = document.getElementById('js-project');
  this.design = document.getElementById('js-design');
  this.development = document.getElementById('js-development');
  this.ejecution = document.getElementById('js-ejecution');

  this.shuffle = new Shuffle(element, {
    easing: 'cubic-bezier(0.165, 0.840, 0.440, 1.000)', // easeOutQuart
    sizer: '.the-sizer',
  });

  this.filters = {
    categories: [],
    services: [],
    areas: [],
    names: [],

    architectures: [],
    constructions: [],

    offices: [],
    retails: [],
    projects: [],
    designs: [],
    developments: [],
    ejecutions: []
  };

  this._bindEventListeners();
};

/**
 * Bind event listeners for when the filters change.
 */
Demo.prototype._bindEventListeners = function () {

  this._onAreaChange = this._handleAreaChange.bind(this);
  this._onNameChange = this._handleNameChange.bind(this);

  this.areas.forEach(function (li) {
    li.addEventListener('click', this._onAreaChange);
  }, this);

  this.names.addEventListener('change', this._onNameChange, false);

  this._onArquitectureChange = this._handleArchitectureChange.bind(this);
  this._onConstructionChange = this._handleConstructionChange.bind(this);

  this.architecture.addEventListener('click', this._onArquitectureChange);
  this.construction.addEventListener('click', this._onConstructionChange);

  this._onOfficeChange = this._handleOfficeChange.bind(this);
  this._onRetailChange = this._handleRetailChange.bind(this);
  this._onProjectChange = this._handleProjectChange.bind(this);
  this._onDesignChange = this._handleDesignChange.bind(this);
  this._onDevelopmentChange = this._handleDevelopmentChange.bind(this);
  this._onEjecutionChange = this._handleEjecutionChange.bind(this);

  this.office.addEventListener('click', this._onOfficeChange);
  this.retail.addEventListener('click', this._onRetailChange);
  this.project.addEventListener('click', this._onProjectChange);
  this.design.addEventListener('click', this._onDesignChange);
  this.development.addEventListener('click', this._onDevelopmentChange);
  this.ejecution.addEventListener('click', this._onEjecutionChange);

};

Demo.prototype._handleOfficeChange = function (evt) {

  var li = evt.currentTarget;

  if (li.classList.contains('active')) {
    li.classList.remove('active');
  } else {
    this.services.forEach(function (li) {
      li.classList.remove('active');
    });

    li.classList.add('active');
  }

  this.filters.offices = li.classList.contains('active') ? [li.getAttribute('data-value')] : [];
  this.filters.retails = [];
  this.filters.projects = [];
  this.filters.designs = [];
  this.filters.developments = [];
  this.filters.ejecutions = [];
  this.filter();

}

